- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    // ensure this is the Seque which is leading to the detail view
    if ([[segue identifier] isEqualToString:@"ShowMapDetail"])
    {
        // now setup detail controller so it can function...
        // get the Detail instance
        myDetailViewController* detail = [segue destinationViewController];
        // get the selected row and city name from it
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        // inform Detail View of selected city
        [detail setDetailItem:[cities objectAtIndex:indexPath.row]];
    }    
}


